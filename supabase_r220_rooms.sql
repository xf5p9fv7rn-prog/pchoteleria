-- ── PASO 3: Verificación — resumen por piso ───────────────────────────────────
SELECT
  b.name       AS edificio,
  r.floor      AS piso,
  COUNT(*)     AS habitaciones,
  SUM(r."bedCount") AS camas,
  MIN(r.id)    AS id_desde,
  MAX(r.id)    AS id_hasta
FROM rooms r
JOIN buildings b ON b.id = r."buildingId"
WHERE b.name ILIKE '%220%' OR b.code ILIKE '%220%'
GROUP BY b.name, r.floor
ORDER BY r.floor;

-- ── PASO 4: Verificación — número e ID de cada habitación ────────────────────
